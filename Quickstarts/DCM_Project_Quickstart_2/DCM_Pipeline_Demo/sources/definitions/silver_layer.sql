select 
    MD5(FIN.CIK::STRING || DATE_PART(epoch_second, FIN.PTS)::STRING) AS SK_COMPANY_ID
	, FIN.PTS AS LAST_UPDATE_TS
	, FIN.CIK AS COMPANY_ID
	, DIM_ST.ST_NAME AS STATUS
	, FIN.COMPANY_NAME AS NAME
	, DIM_I.IN_NAME AS INDUSTRY
	, FIN.SP_RATING
	, CASE WHEN (UPPER(SUBSTR(FIN.SP_RATING,1,1)) = 'A' OR UPPER(SUBSTR(FIN.SP_RATING,1,3)) = 'BBB') THEN 0 ELSE 1 END AS IS_LOW_GRADE
	, FIN.CEO_NAME AS CEO
	, FIN.ADDR_LINE1
	, FIN.ADDR_LINE2
	, FIN.POSTAL_CODE
	, FIN.CITY
	, FIN.STATE_PROVINCE AS STATE_PROV
	, FIN.COUNTRY
	, FIN.DESCRIPTION
	, FIN.FOUNDING_DATE
	, FIN.SC2_START_DATE
	, FIN.SC2_END_DATE
from 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.FINWIRE_CMP_ODS FIN
JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_STATUS_TYPE DIM_ST 
    ON FIN.STATUS = DIM_ST.ST_ID 
JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_INDUSTRY DIM_I 
    ON FIN.INDUSTRY_ID = DIM_I.IN_ID
; 
