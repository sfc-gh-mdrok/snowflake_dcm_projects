-- Create Trade ODS table
define dynamic table DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.TRADE_ODS 
target_lag='DOWNSTREAM' 
warehouse='DCM_DEMO_2_FINANCE_WH{{env_suffix}}' 
AS
select 
    T.CDC_FLAG
	, T.CDC_DSN
	, T.T_ID
	, T.T_DTS
	, TH.TH_DTS
	, COALESCE(TH.TH_DTS,T.T_DTS) AS DT_DTS
	, T.T_ST_ID
	, TH.TH_ST_ID
	, COALESCE(TH.TH_ST_ID,T.T_ST_ID) AS DT_ST_ID
	, T.T_TT_ID
	, CASE WHEN (COALESCE(TH.TH_ST_ID,T.T_ST_ID) = 'SBMT' AND T.T_TT_ID IN ('TMB','TMS')) OR COALESCE(TH.TH_ST_ID,T.T_ST_ID) = 'PNDG' THEN COALESCE(TH.TH_DTS,T.T_DTS) ELSE NULL END AS CREATE_DTS
	, CASE WHEN COALESCE(TH.TH_ST_ID,T.T_ST_ID) IN ('CMPT','CNCL') THEN COALESCE(TH.TH_DTS,T.T_DTS) ELSE NULL END AS CLOSE_DTS
	, T.T_IS_CASH
	, T.T_S_SYMB
	, T.T_QTY
	, T.T_BID_PRICE
	, T.T_CA_ID
	, T.T_EXEC_NAME
	, T.T_TRADE_PRICE
	, T.T_CHRG
	, T.T_COMM
	, T.T_TAX
from 
    DCM_DEMO_2.RAW.TRADE_STG T
LEFT OUTER JOIN 
    DCM_DEMO_2.RAW.TRADEHISTORY_STG TH 
    ON T.T_ID = TH.TH_T_ID
;




