LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_TIME CRDT 
    ON DATE_TRUNC('SECOND', TO_TIME(TODS.CREATE_DTS)) = CRDT.TIME_VALUE
LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_DATE CLDD 
    ON DATE_TRUNC('DAY', TODS.CLOSE_DTS) = CLDD.DATE_VALUE
LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_TIME CLDT 
    ON DATE_TRUNC('SECOND', TO_TIME(TODS.CLOSE_DTS)) = CLDT.TIME_VALUE
LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_ACCOUNT DA 
    ON TODS.T_CA_ID = DA.ACCOUNT_ID 
    AND TODS.DT_DTS BETWEEN DA.SC2_START_DATE 
    AND COALESCE(DA.SC2_END_DATE,'2999-12-31')
LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_SECURITY DS 
    ON TODS.T_S_SYMB = DS.SYMBOL
