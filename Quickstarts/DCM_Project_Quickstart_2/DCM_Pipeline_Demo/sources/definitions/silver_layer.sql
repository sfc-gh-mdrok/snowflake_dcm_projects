as
select 
    TODS.T_ID AS TRADE_ID
	, DA.SK_BROKER_ID AS SK_BROKER_ID
	, TODS.DT_DTS AS TRADE_EVENT_TS
	, IFNULL(CRDD.DATE_ID, LAG(CRDD.DATE_ID) IGNORE NULLS OVER (PARTITION BY TODS.T_ID ORDER BY TODS.DT_DTS ASC)) AS SK_CREATE_DATE_ID
	, IFNULL(CRDT.TIME_ID, LAG(CRDT.TIME_ID) IGNORE NULLS OVER (PARTITION BY TODS.T_ID ORDER BY TODS.DT_DTS ASC)) AS SK_CREATE_TIME_ID
	, CLDD.DATE_ID AS SK_CLOSE_DATE_ID
	, CLDT.TIME_ID SK_CLOSE_TIME_ID
	, DST.ST_NAME AS STATUS
	, DTT.TT_NAME AS TYPE
	, TODS.T_IS_CASH AS CASH_FLAG
	, DS.SK_SECURITY_ID AS SK_SECURITY_ID
	, DS.SK_COMPANY_ID AS SK_COMPANY_ID
	, TODS.T_QTY AS QUANTITY
	, TODS.T_BID_PRICE AS BID_PRICE
	, DA.SK_CUSTOMER_ID AS SK_CUSTOMER_ID
	, DA.SK_ACCOUNT_ID AS SK_ACCOUNT_ID
	, TODS.T_EXEC_NAME AS EXECUTED_BY
	, TODS.T_TRADE_PRICE AS TRADE_PRICE
	, TODS.T_CHRG AS FEE
	, TODS.T_COMM AS COMMISSION
	, TODS.T_TAX AS TAX
	, TODS.DT_DTS AS SC2_START_DATE
	, LEAD(TODS.DT_DTS) OVER (PARTITION BY TODS.T_ID ORDER BY TODS.DT_DTS ASC) AS SC2_END_DATE	
