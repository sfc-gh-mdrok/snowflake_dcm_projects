-- Identify if a prospect is a customer or not
define dynamic table DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.PROSPECT_ISCUST_ODS 
target_lag='DOWNSTREAM' 
warehouse='DCM_DEMO_2_FINANCE_WH{{env_suffix}}' 
AS
select 
    T1.AGENCYID
	, CASE WHEN T2.CUSTOMER_ID IS NULL THEN 'FALSE' ELSE 'TRUE' END AS IS_CUSTOMER
from 
    DCM_DEMO_2.RAW.PROSPECT_STG T1
LEFT OUTER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_CUSTOMER_NOW T2 
    ON UPPER(T1.LASTNAME) = UPPER(T2.LAST_NAME) 
    AND UPPER(T1.FIRSTNAME) = UPPER(T2.FIRST_NAME) 
    AND UPPER(T1.ADDRESSLINE1) = UPPER(T2.ADDRESS_LINE_1) 
    AND UPPER(T1.ADDRESSLINE2) = UPPER(T2.ADDRESS_LINE_2) 
    AND UPPER(T1.POSTALCODE) = UPPER(T2.POSTAL_CODE) 
    AND UPPER(T2.STATUS) = 'ACTV'
;




define dynamic table DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DAILY_MARKET_HIGH_LOWS 
target_lag='DOWNSTREAM' 
warehouse='DCM_DEMO_2_FINANCE_WH{{env_suffix}}' 
as
select 
    DM_S_SYMB as SYMBOL
    , DIM_DATE.DATE_ID as DATE
    , DM_HIGH as DAY_HIGH
    , DM_LOW as DAY_LOW
    , MAX(DM_HIGH) OVER (PARTITION BY DM_S_SYMB ORDER BY DM_DATE ROWS BETWEEN 364 PRECEDING AND CURRENT ROW) as YEAR_HIGH
    , MIN(DM_LOW) OVER (PARTITION BY DM_S_SYMB ORDER BY DM_DATE ROWS BETWEEN 364 PRECEDING AND CURRENT ROW) as YEAR_LOW
from 
    DCM_DEMO_2.RAW.DAILYMARKET_STG
INNER JOIN 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DIM_DATE 
    ON DAILYMARKET_STG.DM_DATE = DIM_DATE.DATE_VALUE
ORDER BY 
    1,2
;

        
-- Calculate daily market high low for each symbol
define dynamic table DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DAILY_MARKET_HIGH_LOW_CALC 
target_lag='DOWNSTREAM' 
warehouse='DCM_DEMO_2_FINANCE_WH{{env_suffix}}' 
as
select 
    GET_HIGHS_LOWS.SYMBOL
	, GET_HIGHS_LOWS.DATE
	, GET_HIGHS_LOWS.DAY_HIGH
	, GET_HIGHS_LOWS.YEAR_HIGH
	, REPLACE(MIN(HIGH.DM_DATE)::STRING,'-')::INT AS EARLIEST_HIGH_DATE
	, GET_HIGHS_LOWS.DAY_LOW
	, GET_HIGHS_LOWS.YEAR_LOW
	, REPLACE(MIN(LOW.DM_DATE)::STRING,'-')::INT AS EARLIEST_LOW_DATE
from 
    DCM_DEMO_2_FINANCE{{env_suffix}}.SILVER.DAILY_MARKET_HIGH_LOWS GET_HIGHS_LOWS
INNER JOIN 
    DCM_DEMO_2.RAW.DAILYMARKET_STG HIGH
	ON HIGH.DM_S_SYMB = GET_HIGHS_LOWS.SYMBOL 
    AND HIGH.DM_HIGH = GET_HIGHS_LOWS.YEAR_HIGH
INNER JOIN 
    DCM_DEMO_2.RAW.DAILYMARKET_STG LOW
	ON LOW.DM_S_SYMB = GET_HIGHS_LOWS.SYMBOL 
    AND LOW.DM_LOW = GET_HIGHS_LOWS.YEAR_LOW
GROUP BY 
    1,2,3,4,6,7
ORDER BY 
    1,2
;



